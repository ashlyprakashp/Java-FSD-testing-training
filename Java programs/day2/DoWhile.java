package day2;


public class DoWhile {

	public static void main(String[] args) {
		//print 1 to 10
		int i=1;
		do
		{
			System.out.print(i+",");
			i++;
		}
		while(i<=10);
		System.out.println();
		//to print from 10-1
		i=10;
		do
		{
			System.out.print(i+",");
			i--;
		}
		while(i>=1);
		System.out.println();
		
	}}
	   
