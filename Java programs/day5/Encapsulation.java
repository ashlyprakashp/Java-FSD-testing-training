package day5;

public class Encapsulation {
	private String Name;
	private int ID;
	private int Salary;

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		this.Name = Name;
	}

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = ID;
	}

	public int getSalary() {
		return Salary;
	}

	public void setSalary(int salary) {
		this.Salary = Salary;
	}

}
