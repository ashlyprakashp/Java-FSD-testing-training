package day8;
import java.lang.*;
public class StringCase {

	public static void main(String[] args) {
		String s1="buggati";
		String s2="HONDA";
		System.out.println(s1.toUpperCase());
		System.out.println(s2.toLowerCase());
		System.out.println(s1);         // value s1 still remains same
	}

}
